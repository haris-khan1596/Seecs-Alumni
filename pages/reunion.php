        </span>
          <p align="justify" class="b_text">Quite interestingly, memories are built around those things which are unnoticed at the time, and then it is the same memories that we cherish throughout our lives. As they say, 'the university life is the best time of the entire life of a student, and surely we always miss the jokes, friendships, of this time throughout our lives. NUST-SEECS provided an opportunity to its alumni to revive their sweet memories by arranging the IST ever annual alumni reunion. The colorful function was held on March, 10 th , 2007 on the premises of Electrical &amp; Mechanical Engineering. </p>
