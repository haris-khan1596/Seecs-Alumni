           <p class="b_text">In addition to the selected performers, the stage was set  on fire by the songs and dance performances of the attendees of the event. Such  enthusiasm and excitement was very well appreciated by the audience as well.<br>
             Finally, as the results were announced, following were  the participants who were able to fight out the competition and grab the top 3  positions:</p>
           <ul>
             <li class="b_text">Winner: Muhammad Yousaf [Talent: Singing]</li>
