          <p class="rimm_heading">EVENT REPORT            </p>
          <p> <span class="b_text">With  the first event of its kind, SEECS Alumni Association &nbsp;in collaboration with SEECS Student Government  Association (SGA) &amp; Alooclan, came up to hunt the talent hidden behind the  studious, hardworking and nerdy faces of the people here at SEECS. SEECS Has  Got Talent was an effort to encourage the students, faculty as well as staff of  the institute to exhibit their skills apart from the academic or professional  excellence.</span></p>
          <p><img src="images/seecs-has-got-talent-1.jpg" width="250" height="290" align="right"></p>
          <p class="b_text">             It was a unique fund-raiser, the SEECS Alumni Association  had coined this event a year ago for the collection of funds for the <a href="http://alumni.seecs.nust.edu.pk/saasp">SEECS Alumni Association Scholarship  Program</a>, founded in 2012 to cater to the financial needs of  students in every semester. </p>
           <p class="b_text">             The event was launched on Tuesday, March 18, 2014. That  day, with a showcase of singing and dance performances, the event was declared  open with a cake cutting ceremony. From that day onwards, the SEECS Family was  invited to register for their unique abilities of live performances. The  registrations continued the following week and the people who registered as  participant for the event were asked to submit a short video of length varying  from 30 seconds to 2.5 minutes, presenting their skills and talents.</p>
           <p class="b_text">             The  response from the SEECS Family towards the participation in the event was  overwhelming, as expected and dozens of registrations were received online. The  videos of these registrations were uploaded on the Facebook group of the event  (<a href="https://www.facebook.com/SEECShasgottalent" target="_blank" class="b_link">https://www.facebook.com/SEECShasgottalent</a>).  From the videos uploaded, the top 20 ones with the greatest number of likes  were selected for the Grand Finale.</p>
           <p class="b_text">             Thursday, April 10, 2014, the one evening, when the most  appreciated talent of the SEECS had to be celebrated, was there. Quite a large  number of faculty members, students, staff and alumni attended the event to  watch the marvelous show of talents and wonders SEECS has got.<img src="images/seecs-has-got-talent-2.jpg" width="250" height="173" align="right"></p>
           <p class="b_text">             From  the recitation of naat to the melodious whistling; the singing of songs to the  piano playing; the pencil arts to the martial arts; the break dances to hip hops;  the speed milk drinking and the solving of rubik&rsquo;s cubes under 1 minute; there  was an astonishing amount of talent being staged during the event!<br>
             These performances were judged right there and then by  the following panel of judges:</p>
           <ul>
             <li class="b_text">Mr.  Maajid Maqbool</li>
             <li class="b_text">Sir  Hamid Ali Shehzad</li>
